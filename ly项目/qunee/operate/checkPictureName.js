//判断图元名称是否修改
//部门名称
function checkDepartNames(name1,name2){
	var nodeName = [];
	graph.forEach(function(element){
		nodeName.push(element.name);
	});
	for(var n=0;n<nodeName.length;n++){
		if(nodeName[n] == name1 || nodeName[n] == name2){
			return 1;
		}
	}
}
